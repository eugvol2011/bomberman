def gen_level(woodenboxes=30, monsters=5):
    # generate wooden boxes
    list_of_zeroes = []
    for j in range(2, len(field) - 1 - 2):
        for i in range(2, len(field[j]) - 1 - 2):
            if field[j][i] == 0:
                list_of_zeroes.append((j, i))
    random.shuffle(list_of_zeroes)
    for i in range(1, woodenboxes + 1):
        rnd = random.randint(0, len(list_of_zeroes) - i)
        field[list_of_zeroes[rnd][0]][list_of_zeroes[rnd][1]] = 4
        cell = list_of_zeroes[rnd]
        list_of_zeroes.pop(rnd)
        list_of_zeroes.append(cell)
    # generate monsters
    list_of_zeroes = []
    for j in range(2, len(field) - 1 - 2):
        for i in range(2, len(field[j]) - 1 - 2):
            if field[j][i] == 0:
                list_of_zeroes.append((j, i))
    random.shuffle(list_of_zeroes)
    for i in range(1, monsters + 1):
        rnd = random.randint(0, len(list_of_zeroes) - i)
        field[list_of_zeroes[rnd][0]][list_of_zeroes[rnd][1]] = 5
        cell = list_of_zeroes[rnd]
        list_of_zeroes.pop(rnd)
        list_of_zeroes.append(cell)
    return field
